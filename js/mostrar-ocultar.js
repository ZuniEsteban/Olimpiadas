function  mostrar_presionar_cuenta(){
    var contenedor = document.getElementsByClassName("contenedor")[0];

    if(contenedor.style.display == "none"){
        contenedor.style.display = "block";
    }else{
        contenedor.style.display = "none";
    } 
}
